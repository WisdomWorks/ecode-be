public class ConvertObjectToArray {
    public static int[] convertToIntArr(Object value) {
        return (int[]) value;
    }

    public static float[] convertToFloatArr(Object value) {
        return (float[]) value;
    }

    public static double[] convertToDoubleArr(Object value) {
        return (double[]) value;
    }

    public static char[] convertToCharArr(Object value) {
        return (char[]) value;
    }

    public static boolean[] convertToBooleanArr(Object value) {
        return (boolean[]) value;
    }

    public static String[] convertToStringArr(Object value) {
        return (String[]) value;
    }
}
